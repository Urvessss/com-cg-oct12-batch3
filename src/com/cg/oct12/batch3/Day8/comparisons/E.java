package com.cg.oct12.batch3.Day8.comparisons;

public class E {
int eid;
String name;
double salary;
public E() {
	
}
public E(int eid, String name, double salary) {
	super();
	this.eid = eid;
	this.name = name;
	this.salary = salary;
}
@Override
public String toString() {
	return "E [eid=" + eid + ", name=" + name + ", salary=" + salary + "]";
}
}

